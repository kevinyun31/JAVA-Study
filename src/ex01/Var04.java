package ex01;

public class Var04 {
    public static void main(String[] args) {
        // 문자열
        String s1 = "가 나 다";
        System.out.println(s1);

        // 연결연산자 +
        String name = "홍길동";
        int age = 25;
        System.out.println(name + "의 나이는 " + age + "살 입니다.");

        int idk = 3000;
        System.out.println(idk+"만큼 메롱해");
                        
    }
}
